// Maps and Sets

// Map: Maps store key-value pairs where keys can be any data type.

const map = new Map();
map.set("a", 1);
map.set("b", 2);
console.log(map.get("a"));

// output  : 1


// Set: Sets store unique values of any type.

const set = new Set([1, 2, 3, 3]);
console.log(set);

// Output : 
// Set(3) { 1, 2, 3 }

